// Basic data types

// string
let firstName: string = 'John';
// number
let roll: number = 10;
// boolean
let isAdmin: boolean = true;
// undefined
let u: undefined = undefined;
// null
let y: null = null;

// any type: not suggested
let d;
d = 10;
d = '10';

// predefined types
let p: number;
p = 23;

//  array
let friends: string[] = ['John', 'Jane'];
let eligibleNumbers: number[] = [1, 2, 3];

// tuple > array > order > type of values
let ageName: [number, string, boolean] = [23, 'John', true];
